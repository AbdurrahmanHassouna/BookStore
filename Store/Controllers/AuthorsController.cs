using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using Store.Models;

namespace Store.Controllers
{
    [Authorize]
    public class AuthorsController : Controller
    {
        private BookStore db = new BookStore();


        public JsonResult AutoComplete(string term)
        {
            List<string> result = db.Authors.Where<Author>(b => b.Name.StartsWith(term)&& !b.Name.StartsWith(term)).OrderBy(n => n.Name).Select(n => n.Name).ToList();
            result.AddRange(db.Authors.Where<Author>(b => b.Name.Contains(term)).Select(n => n.Name));
            return Json(result, JsonRequestBehavior.AllowGet);
        }
        // GET: Authors
        [AllowAnonymous]
        public ActionResult Index()
        {

            return View(db.Authors.Where(b => b.Books.Count>0).Include(b => b.Books).ToList());
        }
        [HttpPost]
        [AllowAnonymous]
        public ActionResult Index(string Search)
        {
            var result = db.Authors.Where(b => b.Name.StartsWith(Search) && b.Books.Count>0).Include(b => b.Books).OrderBy(n => n.Name).ToList();
            result.AddRange(db.Authors.Where(b => b.Name.Contains(Search)&& b.Books.Count>0 && !b.Name.StartsWith(Search)).Include(b => b.Books).OrderBy(n => n.Name));
            return View(result);
        }

        // GET: Authors/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Author author = db.Authors.Find(id);
            if (author == null)
            {
                return HttpNotFound();
            }
            return View(author);
        }

        // GET: Authors/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Authors/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "Id,Name")] Author author)
        {
            if (ModelState.IsValid)
            {
                db.Authors.Add(author);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            return View(author);
        }

        // GET: Authors/Edit/5
        public ActionResult Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Author author = db.Authors.Find(id);
            if (author == null)
            {
                return HttpNotFound();
            }
            return View(author);
        }

        // POST: Authors/Edit/5
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "Id,Name")] Author author)
        {
            if (ModelState.IsValid)
            {
                db.Entry(author).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            return View(author);
        }

        // GET: Authors/Delete/5
        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Author author = db.Authors.Find(id);
            if (author == null)
            {
                return HttpNotFound();
            }
            return View(author);
        }

        // POST: Authors/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            Author author = db.Authors.Find(id);
            db.Authors.Remove(author);
            db.SaveChanges();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
